#Create blanks
display = []
for _ in range(word_length):
    display += "_"

while not end_of_game:
    guess = input("Guess a letter: ").lower()
    screen_clear()

    if guess in display:
        print(f"You choose {guess}. It's allready guess by you.")
    #Check guessed letter
